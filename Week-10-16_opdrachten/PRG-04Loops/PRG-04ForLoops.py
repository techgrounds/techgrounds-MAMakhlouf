# Assign a value of 5 to the variable x
x = 5
# for loop with the statement for i in range(10). The range(10) generates a sequence of numbers from 0 to 9, and in each iteration of the loop, the value of i is automatically assigned to the current number in the sequence. We print the value of i in the loop using the print statement.
for i in range(10):
    print("Value of i in the for loop: ", i)
# another for loop with the statement for i in range(50). This loop iterates over the numbers from 0 to 49. In each iteration, we print the result of x * i, which gives the value of x multiplied by the current number i.
for i in range(50):
    print("x * i =", x* i)
